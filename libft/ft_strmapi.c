#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char				*str;
	unsigned long		slen;
	unsigned int		i;

	i = 0;
	if (!s)
		return (0);
	slen = ft_strlen(s);
	str = (char *)malloc(sizeof(char) * slen + 1);
	if (str == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = (*f)(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
