#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned long			i;
	unsigned char			*p;

	p = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		p[i] = c;
		i++;
	}
	return ((void *)p);
}
